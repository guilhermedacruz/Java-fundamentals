
public class Primitivos {

	public static void main(String[] args) {
		//Tipos primitivos (variáveis)
		//tipo booleano (true or false)
	boolean complete = true;
		//tipo char (1 caractere unicode 'Apostrofo')
	char sexo = 'F';
		//tipo byte (números inteiros)
	byte idade = 17;
		//tipo short (numeros inteiros)
	short numero1 = 16780;
	int numero2 = 1968; //padrão para numeros inteiros
		//tipo long (numeros inteiros)***L
	long numero3 = 214748364L;
		//tipo float (numeros não inteiros)***f
	float nota1 = 5.6f;
		//tipo double (numeros não inteiros)
	double nota2 = 7.8; //padrão para números não inteiros
		//tipo String (cadeia de caracteres) "aspas"
			String nome = "Sirlene";
			System.out.println(complete);
			System.out.println(sexo);
			System.out.println(idade);
			System.out.println(numero1);
			System.out.println(numero2);
			System.out.println(numero3);
			System.out.println(nota1);
			System.out.println(nota2);
			System.out.println(nome);
	}
}
